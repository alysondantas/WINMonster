package br.uefs.ecomp.winMonster.model;

public class BinarioDoCaractere {
	private String binario;
	private boolean verificador;
	/**
	 * @return the binario
	 */
	public String getBinario() {
		return binario;
	}
	/**
	 * @param binario the binario to set
	 */
	public void setBinario(String binario) {
		this.binario = binario;
	}
	/**
	 * @return the verificador
	 */
	public boolean isVerificador() {
		return verificador;
	}
	/**
	 * @param verificador the verificador to set
	 */
	public void setVerificador(boolean verificador) {
		this.verificador = verificador;
	}
}
